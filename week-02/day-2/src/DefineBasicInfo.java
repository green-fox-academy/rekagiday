public class DefineBasicInfo {
    public static void main(String[] args) {
        // Define several things as a variable than print their values
        // Your name as a string
        String myName = "Reka";
        System.out.println("My name is " + myName);

        // Your age as an integer
        int myAge = 31;
        System.out.println("I am " + myAge + " years old");

        // Your height in meters as a double
        double myHeight = 1.53;
        System.out.println("My height is: " + myHeight + "m");

        // Wether you are married or not as a boolean
        boolean married = false;
        if (married == false) {
            System.out.println("I am not married.");
        }
    }
}
